"""
Є система з 20 серверів. Кожен сервер працює незалежно з надійністю 95%. 
Система функціонує стабільно, якщо працює хоча б 18 серверів. 
Яка ймовірність що система функціонує стабільно?
"""

from scipy import stats

n = 20      # к-сть серверів
p = 0.95    # ймовірність
binom_servers = stats.binom(n, p)

# Обчислюємо через доповнення
prob_system_works = 1 - binom_servers.cdf(17)

print(f"Система з {n} серверів, надійність кожного {p}")
print(f"P(X <= 17) = {binom_servers.cdf(17):.4f}")
print(f"P(X >= 18) = 1 - P(X <= 17) = {prob_system_works:.4f}")
print(f"\nЙмовірність що система працює: {prob_system_works:.2%}")
