"""
Задача 8. Частинні похідні для простої поверхні
f(x, y) = x2 + y2
Умова
1. Обчислити частинну похідну ∂f/∂x(x, y).
2. Обчислити частинну похідну ∂f/∂y(x, y).
3. Обчислити значення цих частинних похідних у точці (2, 3).
4. Пояснити, що фізично/геометрично означають ці значення в цій точці.
"""

# Задача 8: частинні похідні для f(x, y) = x^2 + y^2

import sympy as sp

# 1) Оголошуємо змінні
x, y = sp.symbols('x y')

# 2) Задаємо функцію
f = x**2 + y**2

# 3) Частинні похідні
df_dx = sp.diff(f, x)
df_dy = sp.diff(f, y)

print("f(x, y) =", f)
print("∂f/∂x =", df_dx)
print("∂f/∂y =", df_dy)

# 4) Значення в точці (2, 3)
point = {x: 2, y: 3}
df_dx_val = df_dx.subs(point)
df_dy_val = df_dy.subs(point)

print("\nУ точці (2, 3):")
print("∂f/∂x(2,3) =", df_dx_val)
print("∂f/∂y(2,3) =", df_dy_val)

# 5) Геометричний сенс (в тексті)
print("\nПояснення:")
print("∂f/∂x(2,3) — нахил поверхні z=f(x,y) у напрямку осі x при фіксованому y=3.")
print("∂f/∂y(2,3) — нахил поверхні z=f(x,y) у напрямку осі y при фіксованому x=2.")
