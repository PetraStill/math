"""
Список суміжності — це структура даних, де для кожної вершини зберігається список її сусідів.
У Python це зручно реалізувати як словник, де ключ — це вершина, а значення — список сусідніх вершин.
"""

adj_list = {
    'A': ['B', 'C'],
    'B': ['A', 'C', 'D'],
    'C': ['A', 'B'],
    'D': ['B']
}

print("Список суміжності:")
for vertex, neighbors in adj_list.items():
    print(f"{vertex}: {neighbors}")

"""
Список ребер — найпростіше представлення, просто масив пар вершин.
"""

edge_list = [('A', 'B'), ('A', 'C'), ('B', 'C'), ('B', 'D')]

print("Список ребер:")
for edge in edge_list:
    print(edge)
