"""
У Python для створення діаграм Венна використовується бібліотека matplotlib_venn. 
Вона надає дві основні функції: venn2 для двох множин та venn3 для трьох множин.

Синтаксис для двох множин:
    venn2([множина1, множина2], ('Назва1', 'Назва2'))

Синтаксис для трьох множин:
    venn3([множина1, множина2, множина3], ('Назва1', 'Назва2', 'Назва3'))
"""


import matplotlib.pyplot as plt
from matplotlib_venn import venn2

# Користувачі, які купили продукт А і продукт Б
bought_A = {'user1', 'user2', 'user3', 'user5', 'user7'}
bought_B = {'user3', 'user4', 'user5', 'user6', 'user8'}

# Будуємо діаграму Венна
venn2([bought_A, bought_B], ('Продукт А', 'Продукт Б'))
plt.title('Покупці продуктів А і Б')
plt.show()

