"""
–ó–∞–¥–∞—á–∞ 1. –û–ø–µ—Ä–∞—Ü—ñ—ó –Ω–∞–¥ –º–Ω–æ–∂–∏–Ω–∞–º–∏

–£ –Ω–∞—Å —î –±–∞–∑–∞ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á—ñ–≤ —Å–µ—Ä–≤—ñ—Å—É.
–ü–æ–∑–Ω–∞—á–∏–º–æ:
    –ú–Ω–æ–∂–∏–Ω–∞ –≤—Å—ñ—Ö –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á—ñ–≤: U={u1,u2,u3,u4,u5,u6}
    –ö–æ—Ä–∏—Å—Ç—É–≤–∞—á—ñ, —è–∫—ñ –ø—ñ–¥–ø–∏—Å–∞–Ω—ñ –Ω–∞ —Ä–æ–∑—Å–∏–ª–∫—É: A={u1,u2,u4,u5}
    –ö–æ—Ä–∏—Å—Ç—É–≤–∞—á—ñ, —è–∫—ñ –∫—É–ø–∏–ª–∏ –ø–ª–∞—Ç–Ω—É –ø—ñ–¥–ø–∏—Å–∫—É: B={u2,u3,u5}
"""

from itertools import permutations
from itertools import combinations
from itertools import product
U = {"u1", "u2", "u3", "u4", "u5", "u6"}
A = {"u1", "u2", "u4", "u5"}
B = {"u2", "u3", "u5"}

# –ó–Ω–∞–π—Ç–∏ –º–Ω–æ–∂–∏–Ω—É –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á—ñ–≤, —è–∫—ñ –∞–±–æ –ø—ñ–¥–ø–∏—Å–∞–Ω—ñ –Ω–∞ —Ä–æ–∑—Å–∏–ª–∫—É,
# –∞–±–æ –º–∞—é—Ç—å –ø–ª–∞—Ç–Ω—É –ø—ñ–¥–ø–∏—Å–∫—É (–∞–±–æ —ñ —Ç–µ, —ñ —Ç–µ)

either = A.union(B)        # –∞–±–æ A | B
print(either)

# –ó–Ω–∞–π—Ç–∏ –º–Ω–æ–∂–∏–Ω—É –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á—ñ–≤, —è–∫—ñ –æ–¥–Ω–æ—á–∞—Å–Ω–æ —ñ –ø—ñ–¥–ø–∏—Å–∞–Ω—ñ –Ω–∞ —Ä–æ–∑—Å–∏–ª–∫—É,
# —ñ –º–∞—é—Ç—å –ø–ª–∞—Ç–Ω—É –ø—ñ–¥–ø–∏—Å–∫—É

inter = A.intersection(B)  # –∞–±–æ A & B
print(inter)

# –ó–Ω–∞–π—Ç–∏ –º–Ω–æ–∂–∏–Ω—É –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á—ñ–≤, —è–∫—ñ –ø—ñ–¥–ø–∏—Å–∞–Ω—ñ –Ω–∞ —Ä–æ–∑—Å–∏–ª–∫—É,
# –∞–ª–µ –Ω–µ –º–∞—é—Ç—å –ø–ª–∞—Ç–Ω–æ—ó –ø—ñ–¥–ø–∏—Å–∫–∏
# –†—ñ–∑–Ω–∏—Ü—è A –±–µ–∑ B (—â–æ —î –≤ –ê, —á–æ–≥–æ –Ω–µ–º–∞ –≤ B)

C = A.difference(B)     # –∞–±–æ A - B
print("A - B:", C)

"""
–ó–∞–¥–∞—á–∞ 2. –ü—ñ–¥–º–Ω–æ–∂–∏–Ω–∏ —Ç–∞ —Ä—ñ–≤–Ω—ñ—Å—Ç—å –º–Ω–æ–∂–∏–Ω
–£–º–æ–≤–∞
–†–æ–∑–≥–ª—è–Ω–µ–º–æ –º–Ω–æ–∂–∏–Ω–∏:
A={1,2,3,4}
B={2,3}
C={4,3,2,1}
D=‚àÖ
"""

A = {1, 2, 3, 4}
B = {2, 3}
C = {4, 3, 2, 1}
D = set()

# –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ, —á–∏ –≤–∏–∫–æ–Ω—É—î—Ç—å—Å—è B‚äÜA.
# üëâ ¬´–ú–Ω–æ–∂–∏–Ω–∞ B —î –ø—ñ–¥–º–Ω–æ–∂–∏–Ω–æ—é –º–Ω–æ–∂–∏–Ω–∏ A¬ª –∞–±–æ
# üëâ ¬´B –º—ñ—Å—Ç–∏—Ç—å—Å—è –≤ A¬ª

if B <= A:
    print("B‚äÜA")

# –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ, —á–∏ –≤–∏–∫–æ–Ω—É—î—Ç—å—Å—è A = C.

if A == C:
    print("A=C")

# –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ, —á–∏ –≤–∏–∫–æ–Ω—É—î—Ç—å—Å—è D‚äÜA.

if D <= A:
    print("D‚äÜA")

# –ê–±–æ –¥—É–∂–µ –∫–æ—Ä–æ—Ç–∫–æ

print("B‚äÜA:", B <= A)
print("A=C:", A == C)
print("D‚äÜA:", D <= A)


"""
–ó–∞–¥–∞—á–∞ 3. –°–∏–º–µ—Ç—Ä–∏—á–Ω–∞ —Ä—ñ–∑–Ω–∏—Ü—è —Ç–∞ –¥–µ–∫–∞—Ä—Ç—ñ–≤ –¥–æ–±—É—Ç–æ–∫
–£–º–æ–≤–∞
–î–∞–Ω–æ –º–Ω–æ–∂–∏–Ω–∏:
A={1,2,3,4}
B={3,4,5,6}
"""

A = {1, 2, 3, 4}
B = {3, 4, 5, 6}

# –û–±—á–∏—Å–ª—é—î–º–æ —Å–∏–º–µ—Ç—Ä–∏—á–Ω—É —Ä—ñ–∑–Ω–∏—Ü—é –º–Ω–æ–∂–∏–Ω A —Ç–∞ B

C = A.symmetric_difference(B)    # –∞–±–æ A ^ B
print("–°–∏–º–µ—Ç—Ä–∏—á–Ω–∞ —Ä—ñ–∑–Ω–∏—Ü—è: ", C)

# –û–±—á–∏—Å–ª—é—î–º–æ –¥–µ–∫–∞—Ä—Ç—ñ–≤ –¥–æ–±—É—Ç–æ–∫ A√ó{x,y}


XY = {"x", "y"}
cartesian = set(product(A, XY))
print("A √ó {x, y}:", cartesian)

"""
–ó–∞–¥–∞—á–∞ 7. –í–∏–±—ñ—Ä –∫–æ–º–∞–Ω–¥–∏
–£–º–æ–≤–∞
–Ñ 5 —Å—Ç—É–¥–µ–Ω—Ç—ñ–≤: S={‚Äús1‚Äù, ‚Äús2", ‚Äús3‚Äù, ‚Äús4", ‚Äús5‚Äù}

–¢—Ä–µ–±–∞ —Å—Ñ–æ—Ä–º—É–≤–∞—Ç–∏ –∫–æ–º–∞–Ω–¥—É –∑ 2 –æ—Å—ñ–±. –ü–æ—Ä—è–¥–æ–∫ –Ω–µ –º–∞—î –∑–Ω–∞—á–µ–Ω–Ω—è.
–°–∫—ñ–ª—å–∫–∏ —Ä—ñ–∑–Ω–∏—Ö –∫–æ–º–∞–Ω–¥ –º–æ–∂–Ω–∞ —Å—Ç–≤–æ—Ä–∏—Ç–∏?
"""

S = {"s1", "s2", "s3", "s4", "s5"}


