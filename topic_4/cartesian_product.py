# A √ó B = {(a,b)‚à£ a‚ààA —Ç–∞ b‚ààB}
"""
üëâ ¬´–î–µ–∫–∞—Ä—Ç–æ–≤–∏–π –¥–æ–±—É—Ç–æ–∫ –º–Ω–æ–∂–∏–Ω A —ñ B ‚Äî —Ü–µ –º–Ω–æ–∂–∏–Ω–∞ –≤—Å—ñ—Ö —É–ø–æ—Ä—è–¥–∫–æ–≤–∞–Ω–∏—Ö –ø–∞—Ä 
(a,b), –¥–µ 
    a –Ω–∞–ª–µ–∂–∏—Ç—å –º–Ω–æ–∂–∏–Ω—ñ A, –∞ 
    b –Ω–∞–ª–µ–∂–∏—Ç—å –º–Ω–æ–∂–∏–Ω—ñ B¬ª.
"""

"""
–£ Python –¥–µ–∫–∞—Ä—Ç—ñ–≤ –¥–æ–±—É—Ç–æ–∫ –º–æ–∂–Ω–∞ –æ–±—á–∏—Å–ª–∏—Ç–∏ –¥–≤–æ–º–∞ —Å–ø–æ—Å–æ–±–∞–º–∏.
–ü–µ—Ä—à–∏–π —Å–ø–æ—Å—ñ–±, —Ü–µ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è —Ñ—É–Ω–∫—Ü—ñ—ó product –∑ –º–æ–¥—É–ª—è itertools. 
–¶—è —Ñ—É–Ω–∫—Ü—ñ—è —Å—Ç–≤–æ—Ä—é—î –≤—Å—ñ –º–æ–∂–ª–∏–≤—ñ –∫–æ–º–±—ñ–Ω–∞—Ü—ñ—ó –µ–ª–µ–º–µ–Ω—Ç—ñ–≤ –¥–≤–æ—Ö –º–Ω–æ–∂–∏–Ω, 
–ø—ñ—Å–ª—è —á–æ–≥–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç –º–æ–∂–Ω–∞ –ø–µ—Ä–µ—Ç–≤–æ—Ä–∏—Ç–∏ –Ω–∞ –º–Ω–æ–∂–∏–Ω—É.
"""
from itertools import product

A = {1, 2}
B = {'x', 'y', 'z'}

cartesian = set(product(A, B))
print("A √ó B:", cartesian)
print("–ö—ñ–ª—å–∫—ñ—Å—Ç—å –µ–ª–µ–º–µ–Ω—Ç—ñ–≤:", len(cartesian))

"""
–î—Ä—É–≥–∏–π —Å–ø–æ—Å—ñ–± –º–æ–∂–Ω–∞ —Ä–µ–∞–ª—ñ–∑—É–≤–∞—Ç–∏ —á–µ—Ä–µ–∑ set comprehension.
–í—ñ–Ω —Ñ–æ—Ä–º—É—î –¥–µ–∫–∞—Ä—Ç—ñ–≤ –¥–æ–±—É—Ç–æ–∫ —à–ª—è—Ö–æ–º –ø–æ—Å–ª—ñ–¥–æ–≤–Ω–æ–≥–æ –ø–µ—Ä–µ–±–æ—Ä—É 
–≤—Å—ñ—Ö –µ–ª–µ–º–µ–Ω—Ç—ñ–≤ –ø–µ—Ä—à–æ—ó –º–Ω–æ–∂–∏–Ω–∏ —Ç–∞ –≤—Å—ñ—Ö –µ–ª–µ–º–µ–Ω—Ç—ñ–≤ –¥—Ä—É–≥–æ—ó, 
–¥–µ –∫–æ–∂–Ω–∏–π –º–æ–∂–ª–∏–≤–∏–π –≤–∞—Ä—ñ–∞–Ω—Ç –ø–æ—î–¥–Ω–∞–Ω–Ω—è —É—Ç–≤–æ—Ä—é—î –ø–∞—Ä—É.
"""

A = {1, 2}
B = {'x', 'y', 'z'}

cartesian = {(a, b) for a in A for b in B}
print("A √ó B:", cartesian)
