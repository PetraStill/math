"""
–†–æ–∑–º—ñ—â–µ–Ω–Ω—è –∑ n –µ–ª–µ–º–µ–Ω—Ç—ñ–≤ –ø–æ k ‚Äî —Ü–µ –≤–ø–æ—Ä—è–¥–∫–æ–≤–∞–Ω–∏–π –Ω–∞–±—ñ—Ä –∑ 
k –µ–ª–µ–º–µ–Ω—Ç—ñ–≤, –≤–∏–±—Ä–∞–Ω–∏—Ö –∑ –º–Ω–æ–∂–∏–Ω–∏ n —Ä—ñ–∑–Ω–∏—Ö –µ–ª–µ–º–µ–Ω—Ç—ñ–≤.

! –ü–µ—Ä–µ—Å—Ç–∞–Ω–æ–≤–∫–∏ –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—é—Ç—å –≤—Å—ñ n –µ–ª–µ–º–µ–Ω—Ç—ñ–≤, –∞ —Ä–æ–∑–º—ñ—â–µ–Ω–Ω—è –≤–∏–±–∏—Ä–∞—é—Ç—å k –µ–ª–µ–º–µ–Ω—Ç—ñ–≤ –∑ n, 
–¥–µ k‚â§n.
"""

"""
–í—ñ–¥–Ω–æ—à–µ–Ω–Ω—è —Ñ–∞–∫—Ç–æ—Ä—ñ–∞–ª—ñ–≤: (n‚àík)!n!
–ü–æ—è—Å–Ω–µ–Ω–Ω—è –¥–ª—è 10 –¥–æ–ø–æ–≤—ñ–¥–∞—á—ñ–≤ (n) —ñ –ø–µ—Ä—à—ñ 3 (k) –≤–∞–∂–ª–∏–≤—ñ:
    1-—à–µ –º—ñ—Å—Ü–µ: 10 –≤–∞—Ä—ñ–∞–Ω—Ç—ñ–≤
    2-–≥–µ –º—ñ—Å—Ü–µ: 9
    3-—Ç—î –º—ñ—Å—Ü–µ: 8
        ‚Üí 
    10√ó9√ó8=720
"""

import math
from itertools import permutations
import itertools
n = 10   # –≤—Å—å–æ–≥–æ –¥–æ–ø–æ–≤—ñ–¥–∞—á—ñ–≤
k = 3    # –ø–µ—Ä—à—ñ 3 –º—ñ—Å—Ü—è (–ø–æ—Ä—è–¥–æ–∫ –≤–∞–∂–ª–∏–≤–∏–π)

ways = math.factorial(n) // math.factorial(n - k)
print("–ö—ñ–ª—å–∫—ñ—Å—Ç—å –º–æ–∂–ª–∏–≤–∏—Ö –ø–æ—Ä—è–¥–∫—ñ–≤:", ways)
print("~" * 20)

# –Ø–∫—â–æ –ø–æ—Ç—Ä—ñ–±–Ω—ñ —Ä–µ–∞–ª—å–Ω—ñ —Ä–æ–∑–∫–ª–∞–¥–∏, –∞ –Ω–µ —Ç—ñ–ª—å–∫–∏ —á–∏—Å–ª–æ

speakers = [f"Speaker{i}" for i in range(1, 11)]

orders = list(itertools.permutations(speakers, 3))
print("–ö—ñ–ª—å–∫—ñ—Å—Ç—å —É—Å—ñ—Ö –º–æ–∂–ª–∏–≤–∏—Ö —É–ø–æ—Ä—è–¥–∫–æ–≤–∞–Ω–∏—Ö —Ç—Ä—ñ–π–æ–∫ —Ä—ñ–∑–Ω–∏—Ö –¥–æ–ø–æ–≤—ñ–¥–∞—á—ñ–≤ –∑ 10:", len(orders))
print("–ü–µ—Ä—à—ñ 5 –≤–∞—Ä—ñ–∞–Ω—Ç—ñ–≤:", orders[:5])
print("~" * 20)

"""
A^k_n = \frac{n!}{(n-k)!}
–ü–æ–∑–Ω–∞—á–µ–Ω–Ω—è: A^k_n —á–∏—Ç–∞—î—Ç—å—Å—è "A –∑ n –ø–æ k" (k –≤–≥–æ—Ä—ñ —è–∫ —Å—Ç–µ–ø—ñ–Ω—å, n –≤–Ω–∏–∑—É —è–∫ —ñ–Ω–¥–µ–∫—Å)
"""

# Python –º–∞—î —Ñ—É–Ω–∫—Ü—ñ—é math.perm(n, k), —è–∫–∞ –æ–±—á–∏—Å–ª—é—î –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Ä–æ–∑–º—ñ—â–µ–Ω—å –∑ n –ø–æ k.

# 8 —Å–ø–æ—Ä—Ç—Å–º–µ–Ω—ñ–≤, –≤–∏–±–∏—Ä–∞—î–º–æ 3 –ø—Ä–∏–∑–æ–≤—ñ –º—ñ—Å—Ü—è
result = math.perm(8, 3)
print(f"–ö—ñ–ª—å–∫—ñ—Å—Ç—å —Å–ø–æ—Å–æ–±—ñ–≤ —Ä–æ–∑–ø–æ–¥—ñ–ª—É –ø—Ä–∏–∑–æ–≤–∏—Ö –º—ñ—Å—Ü—å: {result}")

# 10 –∫–Ω–∏–≥, –≤–∏–±–∏—Ä–∞—î–º–æ 4 –¥–ª—è –ø–æ–ª–∏—Ü—ñ
books_arrangements = math.perm(10, 4)
print(f"–°–ø–æ—Å–æ–±—ñ–≤ —Ä–æ–∑—Å—Ç–∞–≤–∏—Ç–∏ 4 –∫–Ω–∏–≥–∏ –∑ 10: {books_arrangements}")
print("~" * 20)

# –Ø–∫—â–æ –ø–æ—Ç—Ä—ñ–±–Ω–æ –Ω–µ –ø—Ä–æ—Å—Ç–æ –ø–æ—Ä–∞—Ö—É–≤–∞—Ç–∏, –∞ –æ—Ç—Ä–∏–º–∞—Ç–∏ –≤—Å—ñ –º–æ–∂–ª–∏–≤—ñ –≤–∞—Ä—ñ–∞–Ω—Ç–∏ —Ä–æ–∑–º—ñ—â–µ–Ω—å,
# –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ itertools.permutations(iterable, r).


athletes = ['–ê–Ω–Ω–∞', '–Ü–≤–∞–Ω', '–ú–∞—Ä—ñ—è', '–ü–µ—Ç—Ä–æ']

# –ì–µ–Ω–µ—Ä—É—î–º–æ –≤—Å—ñ –≤–∞—Ä—ñ–∞–Ω—Ç–∏ —Ç–æ–ø-3
top_3_variants = list(permutations(athletes, 3))

print(f"–í—Å—å–æ–≥–æ –≤–∞—Ä—ñ–∞–Ω—Ç—ñ–≤ —Ç–æ–ø-3: {len(top_3_variants)}")

for i, variant in enumerate(top_3_variants, 1):
    gold, silver, bronze = variant
    print(f"{i}. ü•á{gold} ü•à{silver} ü•â{bronze}")


"""
–°–ø–æ–ª—É—á–µ–Ω–Ω—è (–∞–±–æ –∫–æ–º–±—ñ–Ω–∞—Ü—ñ—è) –∑ n –µ–ª–µ–º–µ–Ω—Ç—ñ–≤ –ø–æ k ‚Äî —Ü–µ –Ω–µ–≤–ø–æ—Ä—è–¥–∫–æ–≤–∞–Ω–∏–π –Ω–∞–±—ñ—Ä –∑ k –µ–ª–µ–º–µ–Ω—Ç—ñ–≤, 
–≤–∏–±—Ä–∞–Ω–∏—Ö –∑ –º–Ω–æ–∂–∏–Ω–∏ n —Ä—ñ–∑–Ω–∏—Ö –µ–ª–µ–º–µ–Ω—Ç—ñ–≤.
–ë—ñ–Ω–æ–º—ñ–∞–ª—å–Ω–∏–π –∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç ‚Äî —Ü–µ —á–∏—Å–ª–æ, —è–∫–µ –ø–æ–∫–∞–∑—É—î, —Å–∫—ñ–ª—å–∫–æ–º–∞ —Å–ø–æ—Å–æ–±–∞–º–∏ –º–æ–∂–Ω–∞ –≤–∏–±—Ä–∞—Ç–∏ 
k –µ–ª–µ–º–µ–Ω—Ç—ñ–≤ —ñ–∑ n, –∫–æ–ª–∏ –ø–æ—Ä—è–¥–æ–∫ –Ω–µ –º–∞—î –∑–Ω–∞—á–µ–Ω–Ω—è.
"""

# –î–ª—è —Ä–æ–∑—Ä–∞—Ö—É–Ω–∫—É —Å–ø–æ–ª—É—á–µ–Ω—å –≤ Python —î —Ñ—É–Ω–∫—Ü—ñ—è math.comb(n, k),
# —è–∫–∞ –æ–±—á–∏—Å–ª—é—î –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Å–ø–æ–ª—É—á–µ–Ω—å –∑ n –ø–æ k.


# –õ–æ—Ç–µ—Ä–µ—è: –≤–∏–±—Ä–∞—Ç–∏ 6 —á–∏—Å–µ–ª –∑ 49
lottery = math.comb(49, 6)
print(f"–í–∞—Ä—ñ–∞–Ω—Ç—ñ–≤ –ª–æ—Ç–µ—Ä–µ—ó: {lottery:_}")

# –í–∏–±—Ä–∞—Ç–∏ 3 —Ä–æ–∑—Ä–æ–±–Ω–∏–∫–∏ –∑ 10
team = math.comb(10, 3)
print(f"–í–∞—Ä—ñ–∞–Ω—Ç—ñ–≤ –∫–æ–º–∞–Ω–¥–∏: {team}")


# –Ø–∫—â–æ –ø–æ—Ç—Ä—ñ–±–Ω–æ –æ—Ç—Ä–∏–º–∞—Ç–∏ –≤—Å—ñ –º–æ–∂–ª–∏–≤—ñ –≤–∞—Ä—ñ–∞–Ω—Ç–∏ —Å–ø–æ–ª—É—á–µ–Ω—å, 
# –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ itertools.combinations(iterable, r).

from itertools import combinations

developers = ['–ê–Ω–Ω–∞', '–Ü–≤–∞–Ω', '–ú–∞—Ä—ñ—è', '–ü–µ—Ç—Ä–æ', '–û–ª—å–≥–∞']

# –ì–µ–Ω–µ—Ä—É—î–º–æ –≤—Å—ñ –º–æ–∂–ª–∏–≤—ñ —Ç—Ä—ñ–π–∫–∏
all_teams = list(combinations(developers, 3))

print(f"–í—Å—å–æ–≥–æ –≤–∞—Ä—ñ–∞–Ω—Ç—ñ–≤ –∫–æ–º–∞–Ω–¥: {len(all_teams)}")
for i, team in enumerate(all_teams, 1):
    print(f"{i}. {', '.join(team)}")
